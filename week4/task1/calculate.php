<?php 
class Calculate {
    
    function addNums($first, $second){
        $sum = $first + $second;
        return $sum;
    }

    function subtractNums($first, $second){
        $sum = $first - $second;
        return $sum;
    }

    function divideNums($first, $second){
        $sum = $first / $second;
        return $sum;
    }

    function multiplyNums($first, $second){
        $sum = $first * $second;
        return $sum;
    }
}

?>